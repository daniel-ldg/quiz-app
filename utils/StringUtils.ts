interface RandomCodeOptions {
	digits?: number;
}

export const getRandomCode = (options: RandomCodeOptions = {}) => {
	const { digits = 3 } = options;
	const randomWord = WORDS[Math.floor(Math.random() * WORDS.length)];
	const randomDigits = Math.floor(Math.random() * Math.pow(10, digits))
		.toString()
		.padStart(digits, "0");

	return randomWord + randomDigits;
};
